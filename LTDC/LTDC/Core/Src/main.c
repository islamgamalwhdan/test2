  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  ILI9341_init();
  MX_LTDC_Init();
  Init_SD_RAM();


  Sprite BackGround ;
  Sprite Bird ;
  //Init background sprite
   BackGround.x = 0 ;
   BackGround.y = 0 ;
   BackGround.Width =  LCD_WIDTH ;
   BackGround.Height = LCD_HEIGHT;
   BackGround.ImageData = image_background ;

