    Bird.y = 0 ;
    Bird.Width  = BIRD_WIDTH  ;
    Bird.Height = BIRD_HEIGHT ;
    Bird.vxDelay = 0 ;
    Bird.vyDelay = 0 ;
    Bird.xDelay  = 3 ;
    Bird.yDelay  = 3 ;
    Bird.xSpeed  = 2 ;
    Bird.ySpeed  = 2 ;
    Bird.ImageData = image_angryBird ;

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

     	 DrawSprite(&BackGround , (uint8_t*)BackBuffer ) ;
	     UpdatePostion(&Bird);
	     DrawSprite(&Bird , (uint8_t*)BackBuffer );
	      //swap buffers
	     FlipBuffers(&RenderBuffer , &BackBuffer);
  }
